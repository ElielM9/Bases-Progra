// TIPOS DE DATOS

// Strings - Cadenas de caracteres.
let firstName = `Monserrat`;
let lastName = `Mora`;
let fullName = firstName + ` ` + lastName;

console.log(`Su nombre es: ${fullName}`);

// Numbers - Números
let numberOne = 10;
let numberTwo = 11;
let age = numberOne + numberTwo;

console.log(`Su edad es: ${age}`);

// Booleans - Booleanos
let isActive = true;
let isRunning = false;
let hasPets = true;

console.log(`¿Está activa?`, !isActive); // Negación
console.log(`¿Tiene mascotas?`, hasPets);
